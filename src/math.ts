
/**
 * Subtract two numbers.
 */
export function subtract(a: number, b: number): number {
  return a - b;
}

export function max(a: number, b: number): number {
  return Math.max(a, b);
}

/**
 * Get the square root of a number.
 */
export function sqrt(x: number): number {
  return Math.sqrt(x);
}
